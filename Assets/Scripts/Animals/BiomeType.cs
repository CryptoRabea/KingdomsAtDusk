namespace RTS.Animals
{
    /// <summary>
    /// Defines the different biome/terrain types in the game world.
    /// Used for animal spawning preferences and environmental systems.
    /// </summary>
    public enum BiomeType
    {
        Grassland,      // Open grassy areas
        Forest,         // Dense tree coverage
        River,          // River areas
        Swamp,          // Wetland/marsh areas
        Mountain,       // Rocky/elevated terrain
        Desert,         // Arid/sandy areas
        Tundra,         // Cold/snow-covered areas
        Beach           // Coastal/sandy areas
    }
}
